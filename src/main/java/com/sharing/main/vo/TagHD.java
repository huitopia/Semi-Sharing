package com.sharing.main.vo;

public class TagHD {
	private int thCode;
	private String thName;
	
	public TagHD() {}
	public TagHD(int thCode, String thName) {
		this.thCode = thCode;
		this.thName = thName;
	}
	public int getThCode() {
		return thCode;
	}
	public void setThCode(int thCode) {
		this.thCode = thCode;
	}
	public String getThName() {
		return thName;
	}
	public void setThName(String thName) {
		this.thName = thName;
	}
	
	

}
